  <h1 class="fw-bold mb-4 text-center">Cara Membuat Kopi Enak di Rumah</h1>

  <div class="content-box">
    <p>
      Membuat kopi enak tidak selalu membutuhkan mesin espresso mahal. Dengan alat sederhana sekalipun, 
      kamu bisa mendapatkan rasa yang aromatik dan seimbang jika tekniknya tepat. Inilah panduan lengkap 
      dari barista BeanScape untuk membantumu menyeduh kopi ala kafe di rumah.
    </p>

    <h2 class="mt-4">1. Pilih Biji Kopi yang Segar</h2>
    <p>
      Kesegaran biji kopi adalah faktor utama. Usahakan memilih kopi yang baru disangrai (fresh roasted), 
      biasanya 1–3 minggu setelah roasting adalah waktu terbaik untuk digunakan.
    </p>

    <h2 class="mt-4">2. Tentukan Grind Size Sesuai Metode</h2>
    <p>
      Tingkat kehalusan gilingan sangat mempengaruhi rasa:
      <ul>
        <li><strong>Kasaran</strong>: French Press</li>
        <li><strong>Sedang</strong>: V60, Kalita</li>
        <li><strong>Halus</strong>: Espresso</li>
      </ul>
      Pastikan grind size sesuai metode agar ekstraksi tidak terlalu cepat atau lambat.
    </p>

    <h2 class="mt-4">3. Rasio Kopi dan Air</h2>
    <p>
      Rasio standar seduhan adalah <strong>1:15</strong> (1 gram kopi untuk 15 ml air).  
      Jika ingin lebih kuat, gunakan 1:12. Jika ingin lebih ringan, gunakan 1:17.
    </p>

    <h2 class="mt-4">4. Gunakan Air yang Tidak Terlalu Panas</h2>
    <p>
      Suhu ideal adalah <strong>88–94°C</strong>. Air yang terlalu panas membuat kopi pahit, 
      sementara terlalu dingin membuat kopi flat.
    </p>

    <h2 class="mt-4">5. Teknik Seduh</h2>
    <p>
      Untuk metode manual brew seperti V60:
      <ul>
        <li>Basahi kertas filter lebih dulu.</li>
        <li>Tuang air sedikit untuk proses blooming (30 detik).</li>
        <li>Seduh secara spiral dan perlahan.</li>
      </ul>
    </p>

    <h2 class="mt-4">6. Nikmati Kopi Sesuai Selera</h2>
    <p>
      Kopi enak adalah kopi yang sesuai dengan seleramu.  
      Mau strong? Mau creamy? Mau smooth? Semuanya kembali ke preferensi kamu.
    </p>

